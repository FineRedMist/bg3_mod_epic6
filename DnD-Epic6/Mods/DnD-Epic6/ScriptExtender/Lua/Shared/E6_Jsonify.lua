
---@type string[] -- The members to get from userdata objects
local userdataMembers = {}

---Whether a given property name should be skipped from the output
---@param skipProperties string[] The list of properties to skip
---@param property string The property name to check
---@return boolean True to skip, false otherwise
local function ShouldSkipProperty(skipProperties, property)
    if skipProperties == nil then
        return false
    end
    for _, skipProperty in ipairs(skipProperties) do
        if skipProperty == property then
            return true
        end
    end
    return false
end

---Converts the passed in object such that all userdata is converted to a table of its members (best effort).
---@param obj any
---@return any
function E6_ConvertUserData(obj, skipProperties)
    if obj == nil then
        return nil
    end

    local visited = {}
    local root = { target = obj, path = "", key = nil, container = nil }
    local stack = Dequeue:new()
    stack:pushleft(root)

    while stack:count() > 0 do
        local current = stack:popright()
        local target = current.target
        local path = current.path
        local key = current.key
        local container = current.container
        local value = nil
        local targetType = nil

        local function UpdatedVisited()
            if string.len(path) > 0 then
                visited[target] = path
            else
                visited[target] = "/"
            end
        end

        local function IterateArray(hasMembers)
            local success = true
            local hasvalue = true
            local index = 1
            while success and hasvalue do
                hasvalue = false
                success = pcall(function()
                    local v = target[index]
                    if v then
                        ---@type string|number
                        local safeKey = index
                        if hasMembers then
                            safeKey = tostring(index)
                        end
                        stack:pushleft({ target = v, path = path .. "[" .. tostring(index) .. "]", key = safeKey, container = value })
                        hasvalue = true
                    end
                end)
                index = index + 1
            end
        end
        
        local function ProcessMember(k, v)
            if v then
                if ShouldSkipProperty(skipProperties, k) then
                    value[k] = "<skipped>"
                else
                    stack:pushleft({ target = v, path = path .. "/" .. k, key = k, container = value })
                end
                return true
            end
            return false
        end

        local function ProcessTable()
            UpdatedVisited()
            value = {}
            local hasMembers = false
            for k, v in pairs(target) do
                local memberQueued = ProcessMember(k, v)
                hasMembers = hasMembers or memberQueued
            end
            IterateArray(hasMembers)
        end
        local function ProcessUserData()
            UpdatedVisited()
            value = {}
            local hasMembers = false
            local memberList = userdataMembers
            pcall(function()
                if target.GetAllComponentNames then
                    memberList = target.GetAllComponentNames(true)
                end
            end)
            for _, memberKey in ipairs(memberList) do
                pcall(function()
                    local v = target[memberKey]
                    local memberQueued = ProcessMember(memberKey, v)
                    hasMembers = hasMembers or memberQueued
                end)
            end
            IterateArray(hasMembers)
        end

        if target then
           targetType = type(target)
        end

        local visitedPath = visited[target]
        if not target then
            --_P("Value: nil")
            value = nil
        elseif targetType == "string" or targetType == "number" or targetType == "boolean" then
            value = target
            --_P("Value: " .. tostring(value))
        elseif targetType == "function" then
            value = "<function>"
            --_P("Value: " .. value)
        elseif visitedPath then
            value = "Visited at: " .. visitedPath
            --_P("Value: " .. value)
        elseif targetType == "table" then
            ProcessTable()
            --_P("Value: <table>")
        elseif targetType == "userdata" then
            ProcessUserData()
            --_P("Value: <userdata>")
        else
            _E6Error("Unsupported type: " .. targetType)
        end

        if container then
            container[key] = value
        else
            root.container = value
        end
    end
    return root.container
end

---Converts the passed in object such that all userdata is converted to a table of its members (best effort).
---@param obj any -- The object to convert
---@param skipProperties string[] -- Properties to skip
---@return string -- The converted object as a JSON string
function E6_ToJson(obj, skipProperties)
    _E6P("Converting character data!")
    local userdata = E6_ConvertUserData(obj, skipProperties)
	local opts = {
		MaxDepth = 256
	}
    _E6P("Generating Json!")
    return Ext.Json.Stringify(userdata, opts)
end

---Converts the passed in object such that all userdata is converted to a table of its members (best effort).
---@param obj any -- The object to convert
---@param filename string -- The filename to write the JSON to
---@param skipProperties string[] -- Properties to skip
function E6_ToFile(obj, filename, skipProperties)
    local json = E6_ToJson(obj, skipProperties)
    Ext.IO.SaveFile(filename, json)
end

---Converts the passed in object such that all userdata is converted to a table of its members (best effort).
---@param obj any -- The object to convert
---@param filename string -- The filename to write the JSON to
---@param skipProperties string[] -- Properties to skip
function E6_ToFile_Fast(obj, filename, skipProperties)
    _E6P("Converting character data!")
    local userdata = E6_ConvertUserData(obj, skipProperties)
    _E6P("Writing character data!")
    local fileWriter = Ext.IO.OpenWriter(filename)

    local lastCharIsNewline = false

    ---Writes a value to the file, and tracks if the last character was a newline
    ---@param value string|number|boolean
    local function fileWriterProxy(value)
        if type(value) ~= "string" then
            fileWriter(value)
            lastCharIsNewline = false
        end
        if string.len(value) == 0 then
            return
        end
        local first = string.sub(value, 1, 1)
        while lastCharIsNewline and string.len(value) > 0 and first == "\n" do
            value = string.sub(value, 2)
            first = string.sub(value, 1, 1)
        end
        if string.len(value) > 0 then
            fileWriter(value)
            local last = string.sub(value, string.len(value), 1)
            lastCharIsNewline = last == "\n"
        end
    end

    local writeMap = {}

    local write = function(indent, value)
        local valueType = type(value)
        local writeFunction = writeMap[valueType]
        if writeFunction then
            writeFunction(indent, value)
        else
            _E6Error("Unsupported type: " .. valueType)
        end
    end
    local writeIndent = function(indent)
        if indent > 0 then
            fileWriterProxy(string.rep("\t", indent))
        end
    end

    local writeNull = function(indent, value)
        fileWriterProxy("null")
    end
    local writeRaw = function(indent, value)
        fileWriterProxy(value)
    end
    local writeString = function(indent, value)
        local newValue = string.gsub(value, "\\", "\\\\")
        newValue = string.gsub(newValue, "\"", "\\\"")
        fileWriterProxy("\"", newValue, "\"")
    end

    local writeTable = function(indent, value)
        -- determine if we are empty, an array, or a map
        local firstKey = next(value)
        if firstKey == nil then
            writeRaw(indent, "[]") -- default is to assume an array
            return
        end

        if type(firstKey) == "number" then
            writeRaw(0, "\n")
            writeIndent(indent)
            writeRaw(0, "[\n")
            local len = #value
            for i, v in ipairs(value) do
                writeIndent(indent + 1)
                write(indent + 1, v)
                if i < len then
                    fileWriterProxy(",\n")
                else
                    fileWriterProxy("\n")
                end
            end
            writeIndent(indent)
            writeRaw(indent, "]")
        else
            writeRaw(0, "\n")
            writeIndent(indent)
            writeRaw(0, "{\n")
            -- get the keys, sort them, then iterate in order
            local keys = {}
            for k, _ in pairs(value) do
                table.insert(keys, k)
            end

            table.sort(keys, function(a, b)
                return string.lower(a) < string.lower(b)
            end)

            local len = #keys
            for i, k in ipairs(keys) do
                local v = value[k]
                writeIndent(indent + 1)
                write(indent + 1, k)
                fileWriterProxy(" : ")
                write(indent + 1, v)
                if i < len then
                    fileWriterProxy(",\n")
                else
                    fileWriterProxy("\n")
                end
            end
            writeIndent(indent)
            writeRaw(indent, "}")
        end
    end

    writeMap = {
        ["string"] = writeString,
        ["number"] = writeRaw,
        ["boolean"] = writeRaw,
        ["nil"] = writeNull,
        ["table"] = writeTable
    }

    write(0, userdata)
    _E6P("Character saved!")
end

